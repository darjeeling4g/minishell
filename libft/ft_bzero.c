#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*bf;
	size_t			i;

	i = 0;
	bf = (unsigned char *)s;
	while (i < n)
	{
		bf[i] = 0;
		i++;
	}
}
