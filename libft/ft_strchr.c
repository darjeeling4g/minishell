char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	ch;

	i = 0;
	ch = c;
	while (s[i] != ch)
	{
		if (s[i] == '\0' && ch != '\0')
			return (0);
		i++;
	}
	return ((char *)(&s[i]));
}
